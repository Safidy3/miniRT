#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	while (*s)
	{
		write(fd, s, 1);
		s++;
	}
}

void	ft_putstr_err(char *s)
{
	ft_putstr_fd(s, 2);
}
